            <h3>Ce este depresia?</h3>
            <p>
                Depresia pe scurt poate fi descrisa ca o stare de tristete prelungita 
                cu accese inexplicabile de plâns, irascibilitate, sentimente de inutilitate, 
                de lipsa desperantă, de neajutorare, indiferenta fata de orice.
            </p>
            <p>
                Sunt prezente sentimente de vina nejustificate, activitatile agreabile, munca,
                prietenii care în trecut faceau placere acum nu mai trezesc niciun interes. 
            </p>
            <p>
                Pot aparea diminuari ale capacitatii memoriei, concentrarii, luarii deciziilor, 
                apar gânduri de moarte cu/fara tentative suicidare.<br />
                In plan corporal apare lipsa poftei de mincare sau apetit exagerat, insomnia sau 
                somnolenta excesiva, dureri corporale difuze, articulare musculare, 
                crampe abdominale, diaree/constipatie, manifestari care nu au o 
                cauzalitate medicala obiectiva.
            </p>
            <h3>Care sunt cauzele apariției depresiei?</h3>
            <p>
            Factori considerați ca fiind determinanți în apariția depresiei<br />
            Durerea trăită în urma decesului unei persoane dragi, despărțirea de persoana 
            iubită sau alte pierderi pot determina apariția depresiei. 
            Situațiile stresante din viața noastră, cum ar fi un abuz, pierderea locului de muncă sau schimbarea bruscă a situației financiare pot de asemenea facilita apariția depresiei.<br />
            Drogurile și alcoolul pot determina apariția unui dezechilibru biochimic la nivelul creierului. 
            Acest lucru poate cauza un episod depresiv. Unele persoane depresive 
            recurg la droguri sau alcool pentru "a uita" de necazuri dar nu fac decât să-și agraveze starea depresivă.
            </p>
            <h3>Ce pot face dacă am depresie?</h3>
<p>
            Daca avem semne de intrebare legate de prezenta unei tulburari depresive sau daca persoanele apropiate semnaleaza ca ceva este în neregula cu noi, atunci este oportun sa cerem ajutorul unui specialist (medic psihiatru sau psiholog cu formare în psihoterapie).
</p>
<p>
            Prima vizită poate fi la medicul psihiatru care poate sa spuna daca o persoana sufera sau nu de depresie, care este intensitatea suferintei și ce tratament medicamentos este recomandat pentru afectiunea respectiva. Medicul va informa pacientul despre modul în care actioneaza medicamentele și despre efectele secundare ale acestora.
</p>
<p>
            Fie ca pacientul se hotaraste sau nu sa urmeze un tratament medicamentos, el are optiunea de a urma o psihoterapie  care sa il ajute sa depaseasca problema cu care se confrunta.
</p>
            <h3>Hipnoza se aplica în depresie?</h3>
<p>
            Hipnoza folosita în tratarea depresiei,descopera cauza profunda a tulburarii, în timp ce ajuta persoana sa gaseasca strategii de copping eficiente. Deasemenea, ajuta persoanele depresive sa obtina o stare generala mai buna caracterizata de sentimente de fericire și inlatura sau diminueaza gandurile negative care acompanieaza depresia. Tratamentul hipnoterapeutic pentru aceasta tulburare utilizeaza o combinatie intre sugestii și imagerie pentru a aduce schimbari pozitive în procesul inconstient al persoanei depresive. Persoanele care folosesc hipnoza pentru depresie vor experimenta deseori un sentiment de eliberare și un sentiment puternic al detinerii controlului asupra gandurilor lor și asupra vietii lor în general.
</p>
            <h3>Ce este anxietatea sociala?Fobia sociala?</h3>
            <p>
                Vorbim de fobie socială (sau anxietate socială) atunci când o persoană se 
                simte mereu nesigură, neliniștită, tensionată sau chiar îngrozită 
                în anumite tipuri de situații sociale sau în majoritatea situațiilor sociale. 
            </p>
            <p>
                Fobia socială este o formă a anxietății clinice care se întâlnește la 7-12% din oameni 
                (fiind una dintre cele mai prevalente probleme de anxietate) și debutează 
                de regulă în adolescență sau la începutul vârstei adulte. 
            </p>
            <p>
                În primul rând trebuie să înțelegem că este firesc pentru cei mai mulți 
                oameni ca în anumite situații sociale să se simtă tensionați sau emoționați. 
                De exemplu, poate fi vorba de un interviu, un discurs în fața unui public, 
                o întâlnire cu o persoană dragă, o discuție cu o persoană aflată într-o 
                poziție de autoritate (ex. șeful nostru) sau cunoașterea unor persoane noi. 
                <br />
                În astfel de situații, ni se poate întâmpla adesea să ne bată inima mai tare, 
                să transpirăm, să ne înroșim, să ne tremure vocea sau mâinile. 
                <br />
                Dar aceste lucruri de regulă se reduc pe măsură ce trece timpul și ne 
                acomodăm cu situația în care ne aflăm și nu ne împiedică să facem ceea ce ne-am propus. 
                <br />
                Nu de puține ori ni se întâmplă să ne uităm înapoi la o astfel de 
                situație și să ne zicem: "Am fost puțin emoționat, dar a mers bine! Mi-a plăcut!" 
                sau "M-am cam bâlbâit, dar m-am descurcat eu cumva. N-a fost așa de rău."
</p>
